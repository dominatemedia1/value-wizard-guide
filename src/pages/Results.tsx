
import React, { useEffect, useState } from 'react';
import { useSearchParams } from 'react-router-dom';
import { Card, CardContent } from '@/components/ui/card';
import { Button } from '@/components/ui/button';
import { ArrowLeft, ExternalLink } from 'lucide-react';
import { Link } from 'react-router-dom';
import ResultsDisplay from '@/components/steps/ResultsDisplay';
import { ValuationData } from '@/components/ValuationGuide';
import { decodeUrlData } from '@/utils/urlSharing';

const Results = () => {
  const [searchParams] = useSearchParams();
  const [valuationData, setValuationData] = useState<ValuationData | null>(null);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string>('');

  useEffect(() => {
    console.log('üöÄ Results page loading...');
    console.log('üîç Current URL:', window.location.href);
    console.log('üìã Search params:', Object.fromEntries(searchParams.entries()));
    
    // Try to get data from URL parameters first
    const urlData = decodeUrlData(searchParams);
    if (urlData) {
      console.log('‚úÖ Successfully loaded data from URL parameters');
      setValuationData(urlData);
      setLoading(false);
      return;
    }

    // Fallback to localStorage
    console.log('üîÑ Falling back to localStorage...');
    try {
      const stored = localStorage.getItem('valuationData');
      if (stored) {
        const parsedData = JSON.parse(stored);
        if (parsedData.valuationData) {
          console.log('‚úÖ Successfully loaded data from localStorage');
          setValuationData(parsedData.valuationData);
        } else {
          console.log('‚ùå No valuationData found in stored object');
          setError('No valuation data found');
        }
      } else {
        console.log('‚ùå No data found in localStorage');
        setError('No valuation data found');
      }
    } catch (error) {
      console.error('‚ùå Error loading stored data:', error);
      setError('Error loading valuation data');
    }
    
    setLoading(false);
  }, [searchParams]);

  if (loading) {
    return (
      <div className="min-h-screen flex items-start justify-center pt-[45px] p-4">
        <div className="text-center space-y-4">
          <div className="animate-spin rounded-full h-12 w-12 border-b-2 border-primary mx-auto"></div>
          <p className="text-muted-foreground">Loading your valuation results...</p>
        </div>
      </div>
    );
  }

  if (!valuationData || error) {
    return (
      <div className="min-h-screen flex items-start justify-center pt-[45px] p-4">
        <Card className="max-w-md w-full">
          <CardContent className="p-8 text-center space-y-6">
            <div className="w-16 h-16 bg-muted rounded-full flex items-center justify-center mx-auto">
              <ExternalLink className="w-8 h-8 text-muted-foreground" />
            </div>
            <div className="space-y-2">
              <h1 className="text-2xl font-bold text-foreground">No Results Found</h1>
              <p className="text-muted-foreground">
                {error || 'We couldn\'t find your valuation data. Please start a new valuation.'}
              </p>
            </div>
            <Link to="/">
              <Button className="w-full">
                <ArrowLeft className="w-4 h-4 mr-2" />
                Start New Valuation
              </Button>
            </Link>
          </CardContent>
        </Card>
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-gradient-to-br from-background via-background to-muted/20">
      <div className="container mx-auto px-4 pt-[45px] pb-8 max-w-6xl">
        {/* Header with back button */}
        <div className="mb-8">
          <Link to="/">
            <Button variant="ghost" size="sm" className="mb-4">
              <ArrowLeft className="w-4 h-4 mr-2" />
              Back to Assessment
            </Button>
          </Link>
        </div>

        {/* Results */}
        <div className="space-y-8">
          <ResultsDisplay 
            valuationData={valuationData}
            onSendEmail={() => {}}
          />
        </div>

        {/* Footer */}
        <div className="mt-16 text-center border-t pt-8">
          <p className="text-sm text-muted-foreground">
            Generated by AI Valuation Engine ‚Ä¢ Confidential Analysis
          </p>
        </div>
      </div>
    </div>
  );
};

export default Results;
